    const findProductImage = (productName, category) => {
        const pn = _normalize(productName);
        const config = catMap[category];
        if (!config) return null;

        const { list, path: pathPrefix } = config;

        for (const f of list) {
            const parts = f.split('/');
            const base = parts[parts.length - 1].replace(/\.[^.]+$/, "");
            const fn = _normalize(base);
            if (!fn) continue;
            if (fn.includes(pn) || pn.includes(fn)) {
                const encodedPath = f.split('/').map(p => encodeURIComponent(p)).join('/');
                return `${pathPrefix}${encodedPath}`;
            }
        }

        // Fallback: try raw name with extensions
        try {
            const cleaned = (productName || "").toString().trim();
            if (!cleaned) return null;
            const exts = ['jpg', 'jpeg', 'png', 'webp', 'JPG'];
            for (const ext of exts) {
                const candidate = `${cleaned}.${ext}`;
                // Check if any file in the list matches this candidate (either direct or in subfolder)
                for (const f of list) {
                    if (f === candidate || f.endsWith('/' + candidate)) {
                        const encodedPath = f.split('/').map(p => encodeURIComponent(p)).join('/');
                        return `${pathPrefix}${encodedPath}`;
                    }
                }
            }
        } catch (e) { }

        return null;
    };




